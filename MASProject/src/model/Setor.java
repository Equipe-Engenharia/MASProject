package model;

public class Setor {

	private String identificacao;
	private String nome;

	public String getIdentificacao() {
		return identificacao;
	}

	public void setIdentificacao(String string) {
		this.identificacao = string;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
